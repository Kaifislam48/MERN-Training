let arr = [1, 2, 3, 4, 5];
console.log(arr.join()); // 1,2,3,4,5
console.log(arr.join(""));  // 12345
console.log(arr.join(",")); // 1,2,3,4,5

console.log(arr.reverse()); // [5, 4, 3, 2, 1]
console.log(arr.sort()); // [1, 2, 3, 4, 5]

let nums = [1, 74, 23, 3, 6];
console.log(nums.sort().reverse()); // [74, 23, 6, 3, 1]

 